                    <h4>
                        Introduction
                    </h4>
                    <p>
                        The current hiring environment is incredibly bleak. There is not another way to describe it.
                    </p>
                    <p>
                        Any average job seeker today is aware of this grim reality. Browsing online job boards for hours every week. Indeed, ZipRecruiter, LinkedIn, Dice, Monsterâ€”it doesn't matter. They all seem to blur together at a certain point. The same desolate mural painted with different colors.
                    </p>
                    <p>
                        My story might sound very familiar for some. Raised by a software engineer father, I was always encouraged to pursue a career in technology. "Computers are never going to go away," he told me. And he was correct, of course. So it made sense to me to start studying web development in 2023.
                    </p>
                    <p>
                        After completing my first online course in front end programming, I was ignorant enough to think I was prepared for the field. My father was proud of me and the validation made me believe I was ready to begin applying for jobs. During the following months, I endlessly prowled job postings and got nowhere.
                    </p>
                    <p>
                        It didn't stop me. I continued my education and received six more certifications from two different institutions. Learned five coding languages. I launched three websites in an attempt to build a portfolio and show off my accumulated skills to potential employers. Revised my resume more times than I could keep track of. I was naive enough to think hard work pays off.
                    </p>
                    <p>
                        I tried to follow all the advice I was given. Tailor my resumes in the hopes I could break through the ATS barricades. Be more deliberate in my job search by applying to only positions I knew I was qualified for. Research businesses and follow up with them. Be active and make connections with recruiters on LinkedIn. Try to build a network through family members and friends. Searching for work became a whole new job in itself, occasionally more exhausting than the one I already have.
                    </p>
                    <p>
                        Now, two years later, I am still exactly where I was before. As I write this piece, I sit on a stack of 841 documented job applications. Out of which I have only secured three interviews, all leading to nothing.
                    </p>
                    <p>
                        My unfruitful struggle to begin a new career slowly pushed me into a desperate spiral. I found myself applying to simply any role I thought I had a chance at. Banks, coffee houses, clothing stores, insurance companies, car shops, hotels, construction, and landscaping. Even positions requiring me to take significant pay cuts as well as jobs with long commute times.
                    </p>
                    <p>
                        Only to receive the same generic rejection emails mocking me over and over again. "Unfortunately, your application was not selected at this time."
                    </p>
                    <p>
                        What happened to all the supposed entry level jobs? Where were they? With seven years of experience in the work force, how could it be I was unqualified for literally everything?
                    </p>
                    <p>
                        I realized there were several different aspects to the problem and it wasn't just me. I decided to take a deeper dive into the challenges facing job seekers today and why the current job market is so arduous. I was able to narrow it down to six key reasons, not necessarily in order.
                    </p>
                    <h4>
                        #1. Refusal to train
                    </h4>
                    <p>
                        Companies don't want to invest in their employees any longer. Teaching a new hire the correct approaches in a situation or how to excel in the position is now viewed as a disadvantage since it means spending time, money, and effort. Why provide support for staff when it's not really necessary?
                    </p>
                    <p>
                        By eliminating the expense of training, businesses believe they will generate more revenue. Which might prove to be true in the short term, but can cause dangerous problems in the future. Placing heavier burdens on teams and straining resources to save dollars for the current quarter is not a sustainable solution. It risks manufacturing a product of lesser quality, resentful employees, alienating consumers, and a potentially tarnished reputation.
                    </p>
                    <p>
                        Because of this recent trend, most employers are only focused on highly experienced applicants but are still unwilling to pay fair wages for their knowledgeable credentials. As job seekers grow increasingly desperate, companies have the ability to present lower and lower compensation rates, knowing the candidate will not have many options.
                    </p>
                    <p>
                        I can personally attest to the myriad of job descriptions in the web development field asking for unreasonable requirements. I have seen examples of job descriptions including a master's degree but only paying a $50,000 salary. Or junior roles asking for eight years of experience. I'm certain there are many ridiculous instances existing in other industries as well.
                    </p>
                    <h4>
                        #2. Jobs being offshored
                    </h4>
                    <p>
                        In employers' constant conquest for cheaper labor, they have begun transferring a high number of entry level positions to other countries. India, the Philippines, and Mexico all serve as a highly popular destination for these jobs, where the cost of living is significantly lower. This means businesses can hire even more people for half as much money as an American candidate would need.
                    </p>
                    <p>
                        Offshoring roles sounds like a dream for owners and CEOs but also poses a considerable threat to a large portion of the American working class. The positions getting moved across the ocean are heavily affecting the web development and software engineering fields, along with other various office jobs and customer service roles. But white collar jobs are not the only ones being endangered.
                    </p>
                    <p>
                        Gathering from my almost two years of experience at National Oilwell Varco, the metal casting facility I worked at faced a huge dip in orders due to offshoring. I recall days when there would hardly be enough work to last a whole shift. Even times when I would sweep the ground for hours or cast unneeded parts just to appear busy.
                    </p>
                    <p>
                        The plant manager finally revealed why we stopped receiving orders. They were being sent to India instead. Talk of layoffs also started circulating through departments. Even rumors of shutting down the facility entirely. Thankfully, I was able to land another role before the hammer eventually came down.
                    </p>
                    <p>
                        My story goes to show how the escalation of offshore jobs is bad news for all sectors of American enterprise. According to a Forbes <a href="https://www.forbes.com/sites/jackkelly/2024/10/15/the-globalization-and-offshoring-of-us-jobs-have-hit-americans-hard/" target="_blank" title="Open in new tab">article</a>, an estimated 300,000 positions are being reassigned to outside nations every year. As long as businesses continue to repeatedly outsource their junior roles, it raises an imposing question for the average American job seekerâ€”how will I earn enough to survive?
                    </p>
                    <h4>
                        #3. Advancement of AI
                    </h4>
                    <p>
                        Artificial intelligence has officially landed. It has been shoved in the face of the general public to the point it's basically inescapable. Companies are insistent on incorporating AI into all parts of daily life, from online search engines to vehicles to kitchen appliances. Every business now aggressively advertises their respective AI descendants, perpetually proclaiming its efficiency, precision, and superiority.
                    </p>
                    <p>
                        While there is no doubt AI has helped humanity achieve impressive accomplishments, it has irrevocably complicated the job searching process, which was already challenging enough on its own. But for entry level job seekers, the rise of artificial intelligence could spell utter doom.
                    </p>
                    <p>
                        Technology fields are obviously taking the hardest hits. According to a TechCrunch <a href="https://techcrunch.com/2025/05/15/programmers-bore-the-brunt-of-microsofts-layoffs-in-its-home-state-as-ai-writes-up-to-30-of-its-code/" target="_blank" title="Open in new tab">article</a>, Microsoft recently cut around 6,000 positions, majority of which were software programmers. The company shared AI now generates thirty percent of their code. In another <a href="https://www.hrgrapevine.com/us/content/article/2024-08-08-dell-lays-off-12500-employees-to-become-leaner-in-shift-toward-ai?utm_source=search-list&utm_medium=search&utm_campaign=layoffs" target="_blank" title="Open in new tab">piece</a> from HR Grapevine, Dell announced a mass layoff of 12,500 employees with the intention of a higher focus on artificial intelligence. Google also claims twenty-five percent of their code is composed by AI, as reported in a Fortune <a href="https://fortune.com/2024/10/30/googles-code-ai-sundar-pichai/" target="_blank" title="Open in new tab">article</a>.
                    </p>
                    <p>
                        Even industries outside of tech are suffering. According to a Tech.co <a href="https://tech.co/news/entry-level-roles-at-risk-due-to-ai-study" target="_blank" title="Open in new tab">piece</a>, the great artificial intelligence surge is also making obsolete other entry level roles in administration, customer service, and accounting. The same source also reported the employment rate for people in their early twentiesâ€”between twenty-two and twenty-fiveâ€”has fallen by thirteen percent in the last three years.
                    </p>
                    <p>
                        Aneesh Raman, who is the chief economic opportunity officer at LinkedIn, recently spoke up about this rapidly expanding issue in a Fortune <a href="https://fortune.com/2025/05/25/ai-entry-level-jobs-gen-z-careers-young-workers-linkedin/" target="_blank" title="Open in new tab">article</a>. "While the technology sector is feeling the first wave of change, reflecting AI's mass adoption in this field, the erosion of entry level tasks is expected to play out in fields like finance, travel, food, and professional services too."
                    </p>
                    <p>
                        Another quote from him seems to perfectly encapsulate the conflict facing most job seekers today because of artificial intelligence. "Breaking first is the bottom rung of the career ladder."
                    </p>
                    <h4>
                        #4. Ghost jobs
                    </h4>
                    <p>
                        With nearly all the steps of job applications now being handled through online boards, an additional struggle has emerged. Ghost jobs are positions posted by employers with no intention of hiring. And there are way more of them than people might think.
                    </p>
                    <p>
                        A <a href="https://www.myperfectresume.com/career-center/jobs/search/recruiting-trends" target="_blank" title="Open in new tab">study</a> from 2024 admitted eighty-one percent of recruiters advertise these fake roles, as reported by MyPerfectResume. Further <a href="https://www.thehrdigest.com/the-rise-of-ghost-jobs-is-leaving-job-seekers-frustrated-and-disappointed/" target="_blank" title="Open in new tab">research</a> from The HR Digest revealed forty-eight percent of human resources staff create ghost jobs. According to another <a href="https://www.resumebuilder.com/3-in-10-companies-currently-have-fake-job-posting-listed/" target="_blank" title="Open in new tab">poll</a> completed by ResumeBuilder shared even more evidence of this plague on job seekers by discovering forty percent of businesses posted fake listings in 2024. An <a href="https://www.entrepreneur.com/business-news/one-quarter-of-jobs-posted-online-are-fake-ghost-jobs-study/496683" target="_blank" title="Open in new tab">article</a> from Entrepreneur also showed data explaining over twenty-seven percent of American job openings on LinkedIn are not real.
                    </p>
                    <p>
                        Reasons why employers feel the need to make ghost jobs varies. While still being totally misleading, they can be a helpful method to collect resumes and build talent pools for recruiters. It also can give the impression a company is flourishing from the outside. 
                    </p>
                    <p>
                        On a darker note, it's speculated some hiring managers use ghost jobs as a way to deceive overworked and exhausted teams into believing they will receive future relief. Or even to imbue a fearful mental state in current staff by reminding them how easily they could be replaced. Some businesses will go as far to perform interviews, the candidate unknowingly participating in a game impossible to win.
                    </p>
                    <p>
                        The acceleration of these fake positions is a source of much despair for modern job seekers, especially those at entry level, since it means a large amount of effort in applying is entirely wasted. Because there is no proven manner to spot a ghost job, it has now become just another obstacle to navigate in the treacherous job searching environment.
                    </p>
                    <h4>
                        #5. Economic uncertainty
                    </h4>
                    <p>
                        Hiring rates in 2025 have been significantly suppressed, which is mostly attributed to the recent installation of a new American president and his introduction of heavy tariffs. The current economy has been tossed into an era of deep unease and hesitancy for employers, affecting small companies the hardest.
                    </p>
                    <p>
                        New data released by the US Department of Labor reported a sharp drop in job openingsâ€”a loss of 300,000 in just July and August alone, as summarized in a <a href="https://www.reuters.com/business/us-labor-market-softening-job-openings-hit-10-month-low-hiring-remains-tepid-2025-09-03/" target="_blank" title="Open in new tab">publication</a> from Reuters. Industries facing the largest deficits were healthcare and retail, which had previously been leading areas of employment. Other afflicted fields included arts, entertainment, and professional services.
                    </p>
                    <p>
                        But there was some growth witnessed in other sectors, mostly construction and manufacturing. This data shows there could be a prospective trend in blue collar careers for entry level job seekers, since a majority of roles ask for little to no previous skills or experience.
                    </p>
                    <p>
                        The overall future of the labor market still appears pretty unfortunate at the moment, with layoffs occurring and hiring demands stagnating. An <a href="https://www.cnbc.com/2025/09/24/why-workers-feel-stuck.html" target="_blank" title="Open in new tab">article</a> from CNBC brought attention to the issue of employees now "job hugging" as opposed to "job hopping." This phenomenon combined with the decline of hiring rates is priming the American job market to soon be completely frozen, with less openings than job seekers, and basically locking out any entry level candidates wanting to break into the work force.
                    </p>
                    <h4>
                        #6. Ineffective hiring practices
                    </h4>
                    <p>
                        How businesses hire new staffâ€”if they're truly hiring at allâ€”has changed drastically in modern times and usually not for the better. Professional decorum and the concept of treating potential candidates with respect have both been placed on the back burner since employees are viewed as easily expendable. Hiring managers know there is a large pool of hungry job seekers who will take a bite at anything.
                    </p>
                    <p>
                        Because companies are able to be extremely picky, they have greatly restricted the willingness to take chances on candidates, relating back to the first topic. This means highly skilled and quick learning job seekers are still instantly disqualified simply because past experience may not align one hundred percent with the role they want. So unless a candidate applies for the same exact job they're already doing, the possibility of hearing back from an employer is notably diminished. This attitude is another contributing factor to the "job hugging" seen today and also leaves employed job seekers, who may want to switch career paths, stuck in an unfitting and miserable position.
                    </p>
                    <p>
                        There is now a strong reliance on AI and applicant tracking systems to make work easier for recruiters, due to increasing application volumes. But it also means a huge portion of applications are automatically filtered out if particular words are not detected or other arbitrary criteria is not met, no matter if the job seeker meets every requirement. Even if a candidate is invited for an interview, there is still a high likelihood human eyes have yet to glance at the resume.
                    </p>
                    <p>
                        I've had a sad number of occasions where I received rejection emails for a position almost immediately after submitting the application, despite feeling like I would have been a competent and knowledgeable candidate. But for a substantial amount of applications, most employers cannot even be bothered to send out automated notices, leaving my resume tragically trapped in ATS purgatory.
                    </p>
                    <p>
                        Dragging out the interview process is another test of patience for applicants. A viral <a href="https://www.reddit.com/r/recruitinghell/comments/1ndd0fe/im_on_my_third_interview_for_burger_king/" target="_blank" title="Open in new tab">post</a> on Reddit recounts the story of a candidate who was required to attend three rounds of interviewing for Burger King. Fast food roles, once championed as entry level positions for young job seekers, are now screening candidates in the same way a senior engineer would be. Other unlucky job seekers are being put through multiple interview rounds for junior executive or tech roles, up to seven and beyond. The frustrating process has earned the nickname "<a href="https://www.reddit.com/r/recruitinghell/" target="_blank" title="Open in new tab">recruiting hell</a>" by social media users.
                    </p>
                    <p>
                        These exasperating and sometimes pointless hiring methods inspire despair and low morale in modern job seekers, myself suffering from the same emotions. HR staff is often unclear and inconsistent with communication, causing some candidates to withdraw entirely. Finding new work now means subjecting oneself to meaningless personality tests, completing unpaid assessments, or some other type of silly ritual in order to get noticed by a company. The system is undeniably broken. But how is it the fault of job seekers?
                    </p>
                    <h4>
                        Conclusion
                    </h4>
                    <p>
                        So where does this leave me? Where does it leave everyone else facing this fight?
                    </p>
                    <p>
                        More than likely, we'll be found attaching our resume and then manually filling out the same information all over again on an employer's online career site in the hopes somebody decent will see it, but knowing they won't. Praying for the next job post to finally be the one to break the cycle, only to receive the rejection email in six daysâ€”if at all. "Unfortunately, we have decided to move forward with other candidates." And then watching the same role be reposted a week later.
                    </p>
                    <p>
                        Entry level jobs are becoming a luxury. Getting a new job equivalent to winning the lottery. Hiring seems to happen at random, contingent on connections, luck, and timing instead of based on merit, skills, and qualifications. And now being laid off feels like a constant threat looming in the distance.
                    </p>
                    <p>
                        My dreams of once being a web developer feel so innocent and childish. Between the intense competition of the industry, mass migration of American opportunities to India, and growing dependence on AI, the chance of me ever getting hired in the field is essentially none. I am still learning how to be defeated with dignity.
                    </p>
                    <p>
                        I despise the idea of having to settle for my career but I don't have a choice in the matter. Along with the crowd of job seekers, I will have to take anything I can get in order to survive.
                    </p>
